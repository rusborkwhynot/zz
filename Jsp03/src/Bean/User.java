package Bean;

public class User {
	
	private int id;
	private String subname;
	private String subpwd;
	private String onoffline;
	private String subclassification;
	private String subpayment;
	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public String getSubname() {
		return subname;
	}
	
	public void setSubname(String subname) {
		this.subname = subname;
	}
	
	public String getSubpwd() {
		return subpwd;
	}
	
	public void setSubpwd(String subpwd) {
		this.subpwd = subpwd;
	}
	
	public String getOnoffline() {
		return onoffline;
	}
	
	public void setOnoffline(String onoffline) {
		this.onoffline = onoffline;
	}
	
	public String getSubclassification() {
		return subclassification;
	}
	
	public void setSubclassification(String subclassification) {
		this.subclassification = subclassification;
	}
	
	public String getSubpayment() {
		return subpayment;
	}
	
	public void setSubpayment(String subpayment) {
		this.subpayment = subpayment;
	}
	
}